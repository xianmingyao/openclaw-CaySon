def parse_bilibili_from_text():
    """从页面文本解析B站AI视频数据"""
    
    items = []
    
    # 读取第一页文本
    f1 = os.path.join(DATA_DIR, "bilibili_scrape_101.txt")
    if os.path.exists(f1):
        with open(f1, "r", encoding="utf-8") as f:
            content1 = f.read()
    
    # 读取滚动后文本
    f2 = os.path.join(DATA_DIR, "bilibili_scroll2.txt")
    if os.path.exists(f2):
        with open(f2, "r", encoding="utf-8") as f:
            content2 = f.read()
    
    combined = content1 + content2
    
    # 用正则提取视频条目
    # 模式: 标题 + UP主 + 播放 + 弹幕
    pattern = r'【([^】]+)】([^·\n]+)·(\d{2}-\d{2}|\d+小时前?|昨天|\d+-\d{2})\s*\n?([\d.\u4e00-\u9fff]+)\s*\n?([\d.\u4e00-\u9fff]+)\s*\n?([\d:\u4e00-\u9fff]+)'
    
    # 简单行解析 - 按换行分段
    lines = combined.split('\n')
    
    video_items = []
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        # 检测视频标题（包含AI或相关关键词）
        if any(kw in line for kw in ['AI', '人工智能', 'ChatGPT', 'GPT', '深度学习', '机器学习', '神经网络', '大模型', 'LLM', 'StableDiffusion', 'Midjourney', 'Cursor', 'Claude', 'OpenAI', 'Gemini', 'Copilot']):
            title = line
            # 找UP主
            up = ""
            views = ""
            danmaku = ""
            duration = ""
            
            # 向前找UP主
            for j in range(max(0, i-3), i):
                prev = lines[j].strip()
                if '·' in prev and not any(c in prev for c in ['万', '播放', '弹幕']):
                    up = prev
                    break
            
            # 向后找播放/弹幕/时长
            for j in range(i+1, min(len(lines), i+8)):
                nxt = lines[j].strip()
                if '万' in nxt and not duration:
                    # 可能是播放量或时长
                    if ':' in nxt:
                        duration = nxt
                    else:
                        views = nxt
                elif ':' in nxt and len(nxt) < 20:
                    duration = nxt
                elif '弹幕' in nxt:
                    danmaku = nxt.replace('弹幕', '')
                elif re.match(r'^[\d.]+万?$', nxt) and not views:
                    views = nxt
            
            if title and up:
                video_items.append({
                    'title': title,
                    'up': up.split('·')[0].strip(),
                    'date': up.split('·')[1].strip() if '·' in up else '',
                    'views': views,
                    'danmaku': danmaku,
                    'duration': duration
                })
        i += 1
    
    print(f"B站解析到 {len(video_items)} 个可能相关视频")
    return video_items

